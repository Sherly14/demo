# Identity Operators
# is     - returns True if both the variables are referring to the same object
# is not - returns True if both the variables are not referring to the same object

no1 = int(input("Enter 1st number : "))
no2 = int(input("Enter 2nd number : "))

is_demo = no1 is no2
print("Is Demo : ",is_demo)

is_not_demo = no1 is not no2
print("Is not demo : ",is_not_demo)

